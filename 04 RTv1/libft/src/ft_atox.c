#include "libft.h"

unsigned int	ft_atox(char const *s)
{
	int				i;
	unsigned int	nb;

	i = 0;
	nb = 0;
	while ((9 <= s[i] && s[i] <= 13) || s[i] == ' ')
		i++;
	i += (s[i] == '+') ? 1 : 0;
	if (s[i] == '0' && (s[i + 1] == 'x' || s[i + 1] == 'X'))
		i += 2;
	else if (s[i] == '#')
		i += 1;
	else
		return (nb);
	while ((ft_isdigit(s[i]) || ('A' <= s[i] && s[i] <= 'F')
		|| ('a' <= s[i] && s[i] <= 'f')) && s[i])
	{
		ft_isdigit(s[i]) ? nb = (nb * 16) + s[i] - '0' : 0;
		('A' <= s[i] && s[i] <= 'F') ? nb = (nb * 16) + s[i] - 'A' + 10 : 0;
		('a' <= s[i] && s[i] <= 'f') ? nb = (nb * 16) + s[i] - 'a' + 10 : 0;
		i++;
	}
	return (nb);
}
