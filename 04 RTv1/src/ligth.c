t_uint		ft_light_sum(t_uint c_a, t_uint c_d, t_uint c_s)
{
	t_uint	r;
	t_uint	g;
	t_uint	b;

	r = ((c_a & 0xFF000000) >> 24) + ((c_d & 0xFF000000) >> 24)
		+ ((c_s & 0xFF000000) >> 24);
	g = ((c_a & 0x00FF0000) >> 16) + ((c_d & 0x00FF0000) >> 16)
		+ ((c_s & 0x00FF0000) >> 16);
	b = ((c_a & 0x0000FF00) >> 8) + ((c_d & 0x0000FF00) >> 8)
		+ ((c_s & 0x0000FF00) >> 8);
	(r > 255) ? r = 255 : 0;
	(g > 255) ? g = 255 : 0;
	(b > 255) ? b = 255 : 0;
	return ((r << 24) + (g << 16) + (b << 8));
}
