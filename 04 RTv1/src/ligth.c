#include "rtv1.h"

t_uint		ft_mult_color(t_uint color, double coef)
{
	t_uint	r;
	t_uint	g;
	t_uint	b;

	r = ((color & 0xFF000000) >> 24) * coef;
	g = ((color & 0x00FF0000) >> 16) * coef;
	b = ((color & 0x0000FF00) >> 8) * coef;
	(r > 255) ? r = 255 : 0;
	(g > 255) ? g = 255 : 0;
	(b > 255) ? b = 255 : 0;
	return ((r << 24) + (g << 16) + (b << 8));
}

