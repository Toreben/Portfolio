#include "rt.h"

void ft_get_color(t_color *col_s, t_uint col_x)
{
	col_s->r = (col_x >> 24) & 0xFF;
	col_s->g = (col_x >> 16) & 0xFF;
	col_s->b = (col_x >> 8) & 0xFF;
	col_s->a = (col_x >> 0) & 0xFF;
}
